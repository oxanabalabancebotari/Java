package Computer;

public class Dimension {
	private int wigth;
	private int height;
	private int depth;
	public Dimension(int wigth, int height, int depth) {
		super();
		this.wigth = wigth;
		this.height = height;
		this.depth = depth;
	}
	public int getWigth() {
		return wigth;
	}
	public int getHeight() {
		return height;
	}
	public int getDepth() {
		return depth;
	}
	

}
