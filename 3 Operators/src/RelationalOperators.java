
public class RelationalOperators {
	public static void main(String[] args) {
		
		int i1=5;
		int i2=10;
		System.out.println(i1==i2);
		System.out.println(i1!=i2);
		System.out.println(i1<=i2);
		System.out.println(i1>=i2);
		
		
		double d1=20.0;
		double d2=80.0;
		double res=d1+d2;
		double res2=(res*25);
		System.out.println(res2);
		double res3=(res2%40);
		System.out.println(res3);
		System.out.println(res3<=20);
		

	}

}
